//Primitve and non-primitive data-types
//Primitive are those data types where a single value is stored to a variable.
// Primitive --> String, Number, boolean, undefined, null

// String
let userName = "Arun Malik";
console.log(userName);
console.log(typeof userName);

//Number
let userAge = 25;
console.log(userAge);
console.log(typeof userAge);

//Boolean
let isValidUser = true;
console.log(typeof isValidUser);

//undefined
let userDegree;
console.log(typeof userDegree);

//null
let userAddress = null;
console.log(userAddress);

